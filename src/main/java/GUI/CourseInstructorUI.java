    private void initTable() {
        tblmodel = new DefaultTableModel();
        tblmodel.setColumnIdentifiers(new String[] {"CourseID  ","Title","PersonID","Lastname","Fistname"});
        tblCourseInstructor.setModel(tblmodel);
    }

    private void loadDataToTable() {
        try {
            CourseInstructorBLL bll = new  CourseInstructorBLL();
            List<CourseInstructor> list = bll.LoadCourseInstructor(WIDTH);
            tblmodel.setRowCount(0);
            for (CourseInstructor it : list) {
                tblmodel.addRow(new Object[]{
                    it.getCourseID(),  it.getTitle(),it.getPersonID(), it.getLastName(), it.getFirstName()
              });
            }
            tblmodel.fireTableDataChanged();
        } catch (Exception e) {
        }
    }
