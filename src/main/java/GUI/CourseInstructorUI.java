    private void btndeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btndeleteActionPerformed
        // TODO add your handling code here:
         if(JOptionPane.showConfirmDialog(parentform, "Bạn có muốn xóa không?", "Hỏi"
            , JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE) == JOptionPane.NO_OPTION)
    {
        return;
        }
        try{
            CourseInstructorBLL bll = new CourseInstructorBLL();
            int courseid = Integer.parseInt(txtcourse.getText());
            int personid = Integer.parseInt(txtpersonID.getText());
            if(bll.deleteCourseInstructor(courseid, personid) ==0){
               txtcourse.setText("");
                txttitle.setText("");
                txtpersonID.setText("");
                txtlastname.setText("");
                txtfirstname.setText("");
                JOptionPane.showMessageDialog(parentform,"Xóa  thất bại" , "Cảnh báo", JOptionPane.ERROR_MESSAGE);
            }else{
                JOptionPane.showMessageDialog(parentform, "Xóa thành công");
                txtcourse.setText("");
                txttitle.setText("");
                txtpersonID.setText("");
                txtlastname.setText("");
                txtfirstname.setText("");
                loadDataToTable();
            }

        }catch(Exception e){
            e.printStackTrace();
            JOptionPane.showMessageDialog(parentform,"Khóa học không tồn tại" , "Cảnh báo", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btndeleteActionPerformed

