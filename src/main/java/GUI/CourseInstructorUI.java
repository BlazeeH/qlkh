    private void tblCourseInstructorMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblCourseInstructorMouseClicked
        // TODO add your handling code here:
         try {
            int row = tblCourseInstructor.getSelectedRow();
            if(row >= 0){
                int cid = (Integer) tblCourseInstructor.getValueAt(row, 0);
                int pid = (Integer) tblCourseInstructor.getValueAt(row, 2);
                CourseInstructorBLL bll = new CourseInstructorBLL();
                //CourseInstructor ci = bll.getCourseInstructor(cid, pid);
                cinow = bll.getCourseInstructor(cid, pid);
                if(cinow != null) {
                    txtcourse.setText(String.valueOf(cinow.getCourseID()));
                    txtpersonID.setText(String.valueOf(cinow.getPersonID()));
                    txttitle.setText(cinow.getTitle());
                    txtlastname.setText(cinow.getLastName());
                    txtfirstname.setText(cinow.getFirstName());
                }
            }
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(parentform, e.getMessage(), "Lỗi", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_tblCourseInstructorMouseClicked

