    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        if(checktxtCourseID())return;
        if(JOptionPane.showConfirmDialog(mainform, "Bạn có muốn xóa không?", "Hỏi"
            , JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE) == JOptionPane.NO_OPTION)
    {
        return;
        }
        try{
            OnlinecourseBLL bll = new OnlinecourseBLL();

            if(bll.deleteOnline(Integer.parseInt(txtCourseID.getText())) ==0){
                txtCourseID.setText("");
                txtCourseTitle.setText("");
                txtCredits.setText("");
                txtDepName.setText("");
                txtUrl.setText("");
                JOptionPane.showMessageDialog(mainform,"Xóa khóa học thất bại" , "Cảnh báo", JOptionPane.ERROR_MESSAGE);
            }else{
                JOptionPane.showMessageDialog(mainform, "Xóa khóa học thành công");
                txtCourseID.setText("");
                txtCourseTitle.setText("");
                txtCredits.setText("");
                txtDepName.setText("");
                txtUrl.setText("");
                loadDataToTable();
            }

        }catch(Exception e){
            e.printStackTrace();
            JOptionPane.showMessageDialog(mainform,"Khóa học không tồn tại" , "Cảnh báo", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

