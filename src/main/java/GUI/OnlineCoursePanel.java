    private void tblonlineMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblonlineMouseClicked
        try {
            int row = tblonline.getSelectedRow();
            if(row >= 0){
                int id = (Integer) tblonline.getValueAt(row, 0);
                OnlinecourseBLL bll = new OnlinecourseBLL();
                OnlineCourse oc = bll.getOnline(id);

                if(oc != null) {
                    txtCourseID.setText(String.valueOf(oc.getCourseID()));
                    txtCourseTitle.setText(oc.getTitle());
                    txtCredits.setText(String.valueOf(oc.getCredits()));
                    txtDepName.setText(oc.getDepartmentName());
                    txtUrl.setText(oc.getUrl());
                }
            }
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(mainform, e.getMessage(), "Lỗi", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_tblonlineMouseClicked

    private void txtSearchKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtSearchKeyReleased
        try{
            OnlinecourseBLL bll = new OnlinecourseBLL();
            List<OnlineCourse> list = bll.findbyTitle(txtSearch.getText());

            initTable();
            for(OnlineCourse it : list){
                tblonlinemodel.addRow(new Object[] {
                    it.getCourseID(),it.getTitle(),it.getCredits(),it.getDepartmentName(), it.getUrl()});
        }

        }catch(Exception e){
            e.printStackTrace();
            JOptionPane.showMessageDialog(mainform,"Khóa học không tồn tại" , "Cảnh báo", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_txtSearchKeyReleased

