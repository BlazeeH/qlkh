    private void loadDataToTable() {
        try {
            OnlinecourseBLL bll = new OnlinecourseBLL();
            List<OnlineCourse> list = bll.LoadOnline(WIDTH);
            tblonlinemodel.setRowCount(0);
            for(OnlineCourse it : list) {
                tblonlinemodel.addRow(new Object[] {
                it.getCourseID(),it.getTitle(),it.getCredits(),it.getDepartmentName(), it.getUrl()});
            }
            tblonlinemodel.fireTableDataChanged();
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
    private boolean checktxtCourseID() throws HeadlessException {
        StringBuilder sb = new StringBuilder();
        if(txtCourseID.getText().equals("")){
            sb.append("Mã khóa học không được để trống");
        }
        if (sb.length() >0) {
            JOptionPane.showMessageDialog(mainform, sb, "Cảnh báo", JOptionPane.ERROR_MESSAGE);
            txtCourseID.setBackground(Color.red);
            return true;
        }
        return false;
    }
